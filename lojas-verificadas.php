        <main id="about-title">
            <h1>O que são lojas verificadas?</h1>
            <span>Entenda mais sobre o processo de verificação de qualidade das lojas do painel do preço.</span>
        </main>
        <main class="about-text">
            <div class="bold">Você provavelmente deve conhecer os selos de verificação de integridade das redes sociais como o Twitter:</div>
            <div class="about-image">
                <img src="./assets/verified.png" alt="Selo de verificado.">
            </div>
            <p>
                O processo no Painel do Preço é exatamente o mesmo! A verificação de qualidade das lojas é feita por um
                <span class="bold">processo de auditoria,</span>
                que consiste em verificar a integridade dos dados de cada loja, como:
            </p>
            <p>
                Relatório recente de avaliações de clientes sobre seu atendimento,
                entregas, preços e qualidades de produtos.
            </p>
            <p>
                O processo de verificação é realizado por um grupo de colaboradores, que analisam os dados
                de cada loja e verificam se a loja é confiável.
                <span class="bold">Se a loja for confiável, um selo de verificação é adicionado no perfil e nas páginas de produto dessa loja.</span> 
            </p>
            <p>
                Se a loja não for confiável, o processo de verificação é interrompido e a loja ficará
                <span class="bold">inelegível para receber o selo de verificação durante um tempo de 31 dias.</span>
            </p>
            <p>
                Caso você esteja ciente dos requisitos e deseja aplicar para o selo de verificado,
                <a href="#" target="_blank">clique aqui para preencher o formulário de verificação.</a>
            </p>
